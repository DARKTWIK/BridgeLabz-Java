// Creating Class with name HarryAgeCalculator indicating the purpose is to 
// calculate Harry's age in the year 2024
class HarryAgeCalculator {
   public static void main(String[] args) {

      // Given values
      int birthYear = 2000;
      int currentYear = 2024;

      // Calculate age
      int age = currentYear - birthYear;

      // Print the result
      System.out.println("Harry's age in 2024 is " + age);
   }
}
